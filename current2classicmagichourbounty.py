def make_result_card(pil_img: Image.Image, meta: dict, rank: int) -> Image.Image:
    """Add a score + metadata caption strip to a painting thumbnail."""
    thumb = pil_img.copy()
    thumb.thumbnail((400, 400))
    w, h = thumb.size

    # Caption strip
    strip_h = 56
    card = Image.new('RGB', (w, h + strip_h), color='#111111')
    card.paste(thumb, (0, 0))
    draw = ImageDraw.Draw(card)

    score = meta['score']
    bar_color = ('#00e676' if score >= 0.65 else
                 '#ffeb3b' if score >= 0.45 else '#ff5252')
    draw.rectangle([(0, h), (w, h + 4)], fill=bar_color)

    line1 = f"#{rank}  {score:.3f}  |  {meta['style']}"
    line2 = f"{meta['genre']}  ·  CLIP:{meta['score_clip']:.2f} Col:{meta['score_color']:.2f} Cmp:{meta['score_comp']:.2f}"
    draw.text((6, h + 6),  line1, fill='white')
    draw.text((6, h + 28), line2, fill='#aaaaaa')

    return card


def gradio_image_search(upload, w_clip, w_color, w_comp, top_k):
    if upload is None:
        return [], "Upload a photo first."
    pil_img = Image.fromarray(upload).convert('RGB') if not isinstance(upload, Image.Image) else upload
    t0 = time.time()
    results = search_by_image(pil_img, top_k=int(top_k),
                               w_clip=w_clip, w_color=w_color, w_comp=w_comp)
    elapsed = time.time() - t0
    gallery = [make_result_card(images_raw[r['_idx']], r, i+1)
               for i, r in enumerate(results)]
    summary = (f"⚡ {elapsed:.2f}s  |  {len(results)} matches  |  "
               f"Weights — CLIP:{w_clip:.2f} Color:{w_color:.2f} Comp:{w_comp:.2f}")
    return gallery, summary


def gradio_text_search(query_text, top_k):
    if not query_text.strip():
        return [], "Enter a text description first."
    results = search_by_text(query_text.strip(), top_k=int(top_k))
    gallery = [make_result_card(images_raw[r['_idx']], r, i+1)
               for i, r in enumerate(results)]
    return gallery, f"{len(results)} matches for: \"{query_text}\""


