def clip_embed_images(pil_images: list, model, processor, device, batch_size=32) -> np.ndarray:
    """Batch-encode PIL images with CLIP. Returns L2-normalised float32 array."""
    all_vecs = []
    for i in range(0, len(pil_images), batch_size):
        batch  = pil_images[i:i+batch_size]
        inputs = processor(images=batch, return_tensors='pt', padding=True).to(device)
        with torch.no_grad():
            feats = model.get_image_features(**inputs)
            # The `clip_embed_images` in cell `vUk7SROZaPOI` was already fixed to handle BaseModelOutputWithPooling.
            # Assuming `model.get_image_features` here directly returns a tensor or BaseModelOutputWithPooling
            # whose `pooler_output` is the tensor. Applying the same logic for safety.
            if hasattr(feats, 'pooler_output') and feats.pooler_output is not None:
                feats = feats.pooler_output
            feats = feats / feats.norm(dim=-1, keepdim=True)  # L2 norm
        all_vecs.append(feats.cpu().numpy().astype(np.float32))
    return np.vstack(all_vecs)


def clip_embed_single(pil_img, model, processor, device) -> np.ndarray:
    """Embed a single PIL image. Returns shape (1, dim)."""
    return clip_embed_images([pil_img], model, processor, device, batch_size=1)


def clip_embed_text(text: str, model, processor, device) -> np.ndarray:
    """Encode a text query with CLIP. Returns shape (1, dim)."""
    inputs = processor(text=[text], return_tensors='pt', padding=True).to(device)
    with torch.no_grad():
        feats = model.get_text_features(**inputs)
        # Fix: Access the actual tensor from the BaseModelOutputWithPooling object
        if hasattr(feats, 'pooler_output') and feats.pooler_output is not None:
            feats = feats.pooler_output
        feats = feats / feats.norm(dim=-1, keepdim=True)
    return feats.cpu().numpy().astype(np.float32)


def color_histogram(pil_img: Image.Image, bins: int = 32) -> np.ndarray:
    """Normalised HSV histogram. Returns float32 vector of length bins*3."""
    arr = np.array(pil_img.resize((224, 224)))
    hsv = cv2.cvtColor(arr, cv2.COLOR_RGB2HSV)
    h = cv2.calcHist([hsv], [0], None, [bins], [0, 180]).flatten()
    s = cv2.calcHist([hsv], [1], None, [bins], [0, 256]).flatten()
    v = cv2.calcHist([hsv], [2], None, [bins], [0, 256]).flatten()
    hist = np.concatenate([h, s, v]).astype(np.float32)
    hist /= hist.sum() + 1e-8
    return hist


def composition_features(pil_img: Image.Image, grid: int = 4) -> np.ndarray:
    """Edge-density per grid cell, L2-normalised. Returns float32 vector."""
    arr  = np.array(pil_img.resize((224, 224)))
    gray = cv2.cvtColor(arr, cv2.COLOR_RGB2GRAY)
    edges= cv2.Canny(gray, 50, 150)
    h, w = edges.shape
    ch, cw = h // grid, w // grid
    feats = np.array([
        edges[i*ch:(i+1)*ch, j*cw:(j+1)*cw].mean() / 255.0
        for i in range(grid) for j in range(grid)
    ], dtype=np.float32)
    norm = np.linalg.norm(feats) + 1e-8
    return feats / norm


