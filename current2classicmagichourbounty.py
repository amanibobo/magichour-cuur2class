def search_paintings(
    query_vec:   np.ndarray,
    query_color: np.ndarray,
    query_comp:  np.ndarray,
    top_k:    int   = TOP_K,
    w_clip:   float = W_CLIP,
    w_color:  float = W_COLOR,
    w_comp:   float = W_COMP,
    candidates_mult: int = 5,
) -> list[dict]:
    """
    Multi-dimensional painting search.
    1. Retrieve `top_k * candidates_mult` candidates from CLIP index.
    2. Re-rank by weighted combination of CLIP + color + composition scores.
    """
    # Normalise weights
    total = w_clip + w_color + w_comp
    w_clip, w_color, w_comp = w_clip/total, w_color/total, w_comp/total

    n_cand = min(top_k * candidates_mult, len(paintings_meta))
    clip_scores, clip_idx = clip_index.search(query_vec, n_cand)

    q_color_norm = normalize(query_color.reshape(1, -1), norm='l2').astype(np.float32).flatten()
    q_comp_flat  = query_comp.flatten()

    results = []
    for rank, idx in enumerate(clip_idx[0]):
        idx = int(idx)
        s_clip  = float(clip_scores[0][rank])
        s_color = float(np.dot(q_color_norm, color_norms[idx]))
        s_comp  = float(np.dot(q_comp_flat,  comp_feats[idx]))
        score   = w_clip * s_clip + w_color * s_color + w_comp * s_comp

        results.append({
            **paintings_meta[idx],
            'score'      : round(score,   4),
            'score_clip' : round(s_clip,  4),
            'score_color': round(s_color, 4),
            'score_comp' : round(s_comp,  4),
            '_idx'       : idx,
        })

    results.sort(key=lambda x: x['score'], reverse=True)
    return results[:top_k]


def search_by_image(pil_img: Image.Image, top_k=TOP_K, w_clip=W_CLIP, w_color=W_COLOR, w_comp=W_COMP):
    """Full pipeline: PIL image → ranked painting results."""
    img_rgb = pil_img.convert('RGB')
    q_clip  = clip_embed_single(img_rgb, clip_model, clip_processor, DEVICE)
    q_color = color_histogram(img_rgb)
    q_comp  = composition_features(img_rgb)
    return search_paintings(q_clip, q_color, q_comp, top_k, w_clip, w_color, w_comp)


def search_by_text(query_text: str, top_k=TOP_K):
    """CLIP text-to-painting search (CLIP-only, no color/comp)."""
    q_vec   = clip_embed_text(query_text, clip_model, clip_processor, DEVICE)
    scores, indices = clip_index.search(q_vec, top_k)
    results = []
    for rank, idx in enumerate(indices[0]):
        idx = int(idx)
        results.append({
            **paintings_meta[idx],
            'score'      : round(float(scores[0][rank]), 4),
            'score_clip' : round(float(scores[0][rank]), 4),
            'score_color': 0.0,
            'score_comp' : 0.0,
            '_idx'       : idx,
        })
    return results


